		view = calculate_lookAt_matrix(cameraPos, cameraPos + cameraFront, cameraUp);
		modelLoc = glGetUniformLocation(ourShader.ID, "view");
		glUniformMatrix4fv(modelLoc, 1, GL_FALSE, glm::value_ptr(view));

